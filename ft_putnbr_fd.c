#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int n, int fd)
{
	char c;

	if (n == -2147483648)
	{
		write(fd, "-", 1);
		write(fd, "2", 1);
		n = 147483648;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n = n * -1;
	}
	if (n < 10)
	{
		c = n + 48;
		write(fd, &c, 1);
		return ;
	}
	ft_putnbr_fd((n / 10), fd);
	ft_putnbr_fd((n % 10), fd);
}
